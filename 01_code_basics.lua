-- ============================================================
-- 【对比】Lua vs Python vs Rust vs Go vs C++
-- ============================================================
-- Lua:
--   - -- 单行注释，--[[ ]] 多行注释，--[=[ ]=] 嵌套多行
--   - 缩进不强制，用 end 闭合代码块
--   - 关键字不能用作标识符
--   - nil 是唯一空值，表示"没有值"
--   - require() 加载模块，package.path 搜索路径
--   - 表是唯一复合类型，索引从 1 开始
--   - 第一等函数、闭包、元表是核心特性

-- Python:
--   - # 单行注释，""" 或 ''' 多行注释
--   - 缩进强制（PEP8 标准 4空格）
--   - 关键字不能用作标识符
--   - None 是空值
--   - import 语句加载模块
--   - 列表推导、装饰器、生成器是特色

-- Rust:
--   - // 行注释，/* */ 块注释，/// 文档注释
--   - 缩进不强制（社区建议4空格）
--   - 关键字不能直接用作标识符，r# 前缀可转义
--   - _ 开头变量不触发未使用警告
--   - mod/use 加载模块
--   - Option<T> 替代 null，所有权+借用保证安全

-- Go:
--   - // 单行注释，/* */ 块注释
--   - 缩进由 gofmt 强制统一
--   - 关键字不能用作标识符
--   - nil 是指针/接口/切片/map/chan/函数的零值
--   - import 加载包，package 是组织单元
--   - 错误作为值返回（多返回值），defer 延迟执行

-- C++:
--   - // 单行注释，/* */ 块注释
--   - 缩进由团队约定，无强制
--   - 关键字不能用作标识符（但有 __ 前缀扩展）
--   - nullptr（C++11）是空指针，NULL 是宏
--   - #include 包含头文件，using/namespace 导入命名空间
--   - 模板提供泛型，RAII 管理资源

function compare_with_rust_python()
    -- Lua: 缩进不影响语法，注释不进入运行时
    local msg = "hello from lua"

    -- Lua 的 nil 相当于 Rust 的 None、Python 的 None、Go 的 nil、C++ 的 nullptr
    local empty_value = nil
    print("nil is:", empty_value)
end

