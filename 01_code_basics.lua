-- 【问题4】Lua 的 nil 是什么？为什么重要？
--
-- nil 是 Lua 的"空值"，表示"没有值"
-- nil 用来删除变量（将该位置设为 nil）
--
-- 重要特性：
--   - 全局变量未赋值默认为 nil
--   - table 中访问不存在的键返回 nil
--   - nil 是"假"值（falsy）
--   - 只有一个 nil 值

local empty = nil
print("nil type:", type(nil))
print("nil is falsy:", not nil)  -- true

-- 删除变量（将其设为 nil）
local temp = 100
temp = nil  -- temp 不再存在（垃圾回收）
print("temp after nil:", temp)  -- nil

