-- 【问题1】为什么要写注释？注释会被保留到运行时吗？
--
-- Lua 注释：
--   - 单行注释：--（两个减号）
--   - 多行注释：--[[ ... ]] 或 --[=[ ... ]=]
--
-- 从运行角度：注释在词法分析阶段被丢弃，不进入运行时
-- 从机器角度：注释不会生成任何字节码，不影响性能和内存

-- 单行注释示例
-- 这是一个单行注释

--[[
这是一个多行注释
可以跨越多行
]]

--[=[
这是另一种多行注释语法
可以嵌套
]=]

